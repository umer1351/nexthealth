	3. In payment method drop down list, select 'Payment at Easypaisa Shop'.<br>
	4. Provide your mobile number, email address and characters shown in the picture.<br>
	5. Click on the Proceed button.<br>
	6. You will be provided with a Payment Token Number.<br>
	7. Please note down your token number, which has also been sent to your cell phone number / email address.<br>
	8. Visit any nearby EasyPaisa Retailer / Franchise.<br>
	9. Provide your Token number and Cell phone number to the Retailer / Franchise.<br>
	10. The retailer will make a payment on your behalf by selecting "EasyPay" in the bill payment menu.<br>
	11. After payment, you will receive an SMS confirmation which will contain unique Transaction ID, Paid Amount and Token Number. <br>
