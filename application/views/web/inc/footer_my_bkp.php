               $(".p_m").show(); 
               $("#p_m_id_1").hide();

            }
            total_after_shipping  = shipping_method_price + total_before_shipping;
            
            $("#shipping_price").html(shipping_method_price);  
            $("#total_after_shipping").val(total_after_shipping);
            
            $("#show_grand_total_price").text(total_after_shipping);
            $("#shipping_price_for_order_tbl").val(price_slot_id);
